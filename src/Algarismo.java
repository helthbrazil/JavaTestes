
public class Algarismo {
	private int valor;
	private String algarismo;

	public Algarismo(int valor, String algarismo) {
		super();
		this.valor = valor;
		this.algarismo = algarismo;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	public String getAlgarismo() {
		return algarismo;
	}

	public void setAlgarismo(String algarismo) {
		this.algarismo = algarismo;
	}


}
